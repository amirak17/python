print('slice')
b = "Hello, World!"
print(b[2:5]) # llo
print('\n----\n')

print('slice from the start')
b = "Hello, World!"
print(b[:5]) # Hello
print('\n----\n')

print('slice to the end')
b = "Hello, World!"
print(b[2:]) # llo, World!
print('\n----\n')

print('negative indexing')
b = "Hello, World!"
print(b[-5:-2]) # orl
print('\n----\n')
