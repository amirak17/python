# upper case
a = "Hello, World!"
print(a.upper())
print('\n----\n')

# lower case
a = "Hello, World!"
print(a.lower())
print('\n----\n')

# remove whitespace
a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"
print('\n----\n')

# replace string
a = "Hello, World!"
print(a.replace("H", "J"))
print('\n----\n')

# split string
a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!']
print('\n----\n')
