print('upper case')
a = "Hello, World!"
print(a.upper())
print('\n----\n')

print('lower case')
a = "Hello, World!"
print(a.lower())
print('\n----\n')

print('remove whitespace')
a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"
print('\n----\n')

print('replace string')
a = "Hello, World!"
print(a.replace("H", "J"))
print('\n----\n')

print('split string')
a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!']
print('\n----\n')
