b = "Hello, World!"
print(b[2:5]) # llo
print('\n----\n')

# Slice From the Start
b = "Hello, World!"
print(b[:5]) # Hello
print('\n----\n')

# Slice To the End
b = "Hello, World!"
print(b[2:]) # llo, World!
print('\n----\n')

# Negative Indexing
b = "Hello, World!"
print(b[-5:-2]) # orl
print('\n----\n')
